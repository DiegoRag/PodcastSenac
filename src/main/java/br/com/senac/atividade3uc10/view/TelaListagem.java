    public void ajustarVisibilidadeElementos() {
        if (TelaLog.isIsAdmin()) {
            // Mostrar elementos específicos para administradores
            btnExcluir.setEnabled(true);
            BtnCadastro.setEnabled(true);
            BtnPesquisar.setEnabled(true);
        } else if (TelaLog.isIsOperador()) {
            btnExcluir.setEnabled(false);
            BtnCadastro.setEnabled(true);
            BtnPesquisar.setEnabled(true);
        } else if (TelaLog.isIsUsuario()) {
            btnExcluir.setEnabled(false);
            BtnCadastro.setEnabled(false);
            BtnPesquisar.setEnabled(true);
        }

    }

    public void preencheTabela(List<Podcast> podcasts) {
        String colunas[] = {"ID", "Produtor", "Nome Do Episodio", "N do episodio", "Duracao", "URL do repo"};
        String dados[][] = new String[podcasts.size()][colunas.length];

        int i = 0;
        for (Podcast r : podcasts) {
            dados[i] = new String[]{
                String.valueOf(r.getId()),
                r.getProdutor(),
                r.getNome_do_episodio(),
                String.valueOf(r.getNumero_do_episodio()),
                r.getDuracao_do_episodio(),
                r.getUrl_do_repositorio()
            };
            i++;
        }
        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        tblPodcasts.setModel(model);
    }

