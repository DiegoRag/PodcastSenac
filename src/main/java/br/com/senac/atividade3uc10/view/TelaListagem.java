    private void BtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarActionPerformed
        // Obtém o texto inserido no campo de pesquisa
        String produtorPesquisado = PesquisaProdutorTxtField.getText().trim();

        // Verifica se o campo de pesquisa não está vazio
        if (!produtorPesquisado.isEmpty()) {
            PodcastDAO podcastDAO = new PodcastDAO();

            // Realiza a pesquisa pelo produtor
            List<Podcast> podcastsEncontrados = podcastDAO.pesquisarPorProdutor(produtorPesquisado);

            // Atualiza a tabela com os resultados da pesquisa
            preencheTabela(podcastsEncontrados);
        } else {
            // Se o campo de pesquisa estiver vazio, exibe uma mensagem de erro
            JOptionPane.showMessageDialog(this, "Por favor, insira o nome do produtor para pesquisar.", "Campo Vazio", JOptionPane.ERROR_MESSAGE);
        }                        // TODO add your handling code here:
    }//GEN-LAST:event_BtnPesquisarActionPerformed

