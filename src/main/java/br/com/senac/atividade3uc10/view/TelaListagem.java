    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        // Permite a exclusão para Administrador
        String idPodcastString = JOptionPane.showInputDialog(this, "Digite o ID do podcast que deseja excluir:");
        // Verifica se a string não está vazia ou nula antes de tentar converter
        if (idPodcastString != null && !idPodcastString.isEmpty()) {
            int idPodcast = Integer.parseInt(idPodcastString);
            PodcastDAO podcastDAO = new PodcastDAO();
            podcastDAO.excluirPodcastPorId(idPodcast);
            this.repaint();
        } else {
            // Caso o usuário cancele ou não forneça um ID válido
            JOptionPane.showMessageDialog(this, "Nenhum ID fornecido.", "ID Ausente", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

